package chapter08.com.hspedu.homework;

public class Homework02 {
    /*
    * 四种访问修饰符和各自的权限
    * public、protected、默认、private
    * 类的修饰符有两种：public、默认
    * 属性、方法的修饰符有四种
    * public：公开全都可以访问
    * protected：比默认多了子类可访问
    * 默认：同一个包中的类可以访问，子类不能访问
    * private：本类可以访问，子类不能访问
    * 权限范围：public > protected > 默认 > private
    * public：本类、同包、子类、不同包
    * protected：本类、同包、子类
    * 默认：本类、同包
    * private：本类
    * */
}
